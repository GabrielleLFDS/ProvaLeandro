package projeto_funcionarios;

public class ClasseFuncionarios {
	private int atributoIdFuncionario;
	private String atributoNomeFuncionario;
	private double atributoSalarioFuncionario;
	
	public int getAtributoIdFuncionario() {
		return atributoIdFuncionario;
	}
	public void setAtributoIdFuncionario(int atributoIdFuncionario) {
		this.atributoIdFuncionario = atributoIdFuncionario;
	}
	
	public String getAtributoNomeFuncionario() {
		return atributoNomeFuncionario;
	}
	public void setAtributoNomeFuncionario(String atributoNomeFuncionario) {
		this.atributoNomeFuncionario = atributoNomeFuncionario;
	}
	
	public double getAtributoSalarioFuncionario() {
		return atributoSalarioFuncionario;
	}
	public void setAtributoSalarioFuncionario(double atributoSalarioFuncionario) {
		this.atributoSalarioFuncionario = atributoSalarioFuncionario;
	}
}
